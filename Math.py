

# Python has a set of nuilt-in math funtions, including an extensive math module, that allows you to perform mathematical

# Builit-in Math Functions min() and Max()

x = min(10, 20, 30)
y = max(10, 20, 30)

print(x)
print(y)

# Output: 
# 10
# 30


# The abs() function returns the absolute (positive) value of the specified number:

a = abs(-5.12)

print(a)

# Output: 5.12



# The pow(x, y) function return the value of x to the power of y:

x = pow(5, 2)
print(x)

# Output: 25


 
# The Math Module using sqrt() method for root

import math
x = math.sqrt(25)

print(x)

# Output: 5



# The math.ceil() method rounds a number upwards to its nearest integer, and the math.floor() method rounds a number downwards to its nearest integer, and returns the result:

import math

x = math.ceil(2.5) # 3
y = math.floor(2.5) #2

print(x)
print(y)



# The math.pi constant, returns the value of PI (3.14...):

import math
x = math.pi

print(x)

# Output: 3.1415926

