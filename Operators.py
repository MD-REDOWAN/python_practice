# Python has some operators for different purposes:
# 1. Arithmetic Operators: Used for mathematical operations like addition, subtraction, multiplication, and division.
# 2. Comparison Operators: Used to compare values, returning a boolean result (True or False).
# 3. Assignment Operators: Used to assign values to variables.
# 4. Logical Operators: Used to combine conditional statements.
# 5. Bitwise Operators: Used to perform bit-level operations on integers.
# 6. Membership Operators: Used to test if a value is a member of a sequence (like a list or string).
# 7. Identity Operators: Used to check if two variables point to the same object in memory.
# 8. Ternary Operator: A shorthand way to write an if-else statement.
# 9. Lambda Functions: Used to create small anonymous functions.
# 10. List Comprehensions: A concise way to create lists based on existing lists.
# 11. String Formatting: Used to format strings in a readable way.
# 12. Type Casting: Used to convert one data type to another.
# 13. Exception Handling: Used to handle errors gracefully in the code.
# 14. Decorators: Used to modify the behavior of functions or methods.  
# 15. Context Managers: Used to manage resources like files or network connections.
# 16. Generators: Used to create iterators in a memory-efficient way.
# 17. Regular Expressions: Used for pattern matching in strings.
# 18. Importing Modules: Used to include external libraries or modules in your code.
# 19. File I/O: Used to read from and write to files.
# 20. Function Arguments: Used to pass values to functions, including default arguments and variable-length arguments.
# 21. Slicing: Used to extract a portion of a sequence (like a list or string).
# 22. Enumerate: Used to iterate over a sequence while keeping track of the index.
# 23. Zip: Used to combine multiple sequences into a single iterable.
# 24. Map, Filter, and Reduce: Functional programming tools for processing collections.
# 25. Assertions: Used to test conditions in the code and raise errors if they are not met.
# 26. Docstrings: Used to document functions, classes, and modules.
# 27. Type Hints: Used to indicate the expected data types of function arguments and return values.
# 28. F-strings: A way to format strings using expressions inside curly braces.
# 29. List, Set, and Dictionary Comprehensions: Concise ways to create lists, sets, and dictionaries.
# 30. Unpacking: Used to extract values from collections into variables.
